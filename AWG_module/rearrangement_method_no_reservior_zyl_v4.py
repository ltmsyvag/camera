import numpy as np
from itertools import combinations

def Rearrangement_method__no_reservior_zyl_v4(target_array: np.ndarray, state_array: np.ndarray, n_packed: int):
    """
    每一行之间的重排（行内从当前列移动到目标列）
    返回: 分批打包的 move 列表, 每个 move 为 (row_from, col_from, row_to, col_to)
    """
    nrows, ncols = target_array.shape
    complete_moves = []

    # 找到所有需要重排的“非空目标行”
    target_rows = [r for r in range(nrows) if np.sum(target_array[r, :]) > 0]

    for r in target_rows:
        # 该行目标与当前的原子列索引
        target_positions  = [j for j, v in enumerate(target_array[r, :])  if v == 1]
        current_positions = [j for j, v in enumerate(state_array[r, :])   if v == 1]

        if len(target_positions) > len(current_positions):
            print(f'Row {r} is not enough')  # 该行当前原子不足以实现目标
            continue

        n = len(target_positions)
        m = len(current_positions)

        # 穷举当前列位置中取 n 个的所有组合, 匹配到目标列, 选总步数最小且不“跨越”未动原子的方案
        min_steps = float('inf')
        best_move_info = []

        for comb in combinations(range(m), n):
            # 被选中要移动的当前列
            comb_cols = sorted([current_positions[i] for i in comb])
            tgt_cols  = sorted(target_positions)

            steps = 0
            move_info = []
            valid = True

            # 检查移动段上是否存在“未被选中且不移动”的原子（防止跨越碰撞）
            for j, start in enumerate(comb_cols):
                end = tgt_cols[j]

                # 任一未选中的原子位于 (start, end) 之间则视为非法
                for k in range(m):
                    if k not in comb:
                        other = current_positions[k]
                        if min(start, end) < other < max(start, end):
                            valid = False
                            break
                if not valid:
                    break

                move_info.append((r, start, r, end))
                steps += abs(start - end)

            if valid and steps < min_steps:
                min_steps = steps
                best_move_info = move_info

        # 过滤掉不需要移动的（col_from == col_to）
        moves_no_pack = [mv for mv in best_move_info if mv[1] != mv[3]]

        # 打包（每批至多 n_packed 个移动）
        for i in range(0, len(moves_no_pack), n_packed):
            complete_moves.append(moves_no_pack[i:i + n_packed])

    if not complete_moves:
        complete_moves = [[(0, 0, 0, 0)]]
        print('done')

    return complete_moves


def main(state_array: np.ndarray, target_array: np.ndarray, n_packed: int):
    complete_moves = Rearrangement_method__no_reservior_zyl_v4(target_array, state_array, n_packed)
    print(complete_moves)
    return complete_moves


